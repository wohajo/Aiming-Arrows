package components.abstracts;

public abstract class Element {

	int cordX;
	int cordY;

	public int getCordX() {
		return cordX;
	};

	public int getCordY() {
		return cordY;
	};

	public void setCordX(int cordX) {
		this.cordX = cordX;
	};

	public void setCordY(int cordY) {
		this.cordX = cordY;
	};
}
